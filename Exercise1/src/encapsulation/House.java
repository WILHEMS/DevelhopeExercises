package encapsulation;

public class House {
    Integer floorsNumber;
    String address;
    String residentsNames;

    public Integer getFloorsNumber() {
        return floorsNumber;
    }

    public void setFloorsNumber(Integer floorsNumber) {
        this.floorsNumber = floorsNumber;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getResidentsNames() {
        return residentsNames;
    }

    public void setResidentsNames(String residentsNames) {
        this.residentsNames = residentsNames;
    }
}
